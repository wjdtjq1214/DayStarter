public class localCode {
    private String localCode;
    private String localName;
    private int x;
    private int y;
    private String upLocalCode;
    private String upLocalName;

    public String getLocalCode() {
        return localCode;
    }

    public void setLocalCode(String localCode) {
        this.localCode = localCode;
    }

    public String getLocalName() {
        return localName;
    }

    public void setLocalName(String localName) {
        this.localName = localName;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public String getUpLocalCode() {
        return upLocalCode;
    }

    public void setUpLocalCode(String upLocalCode) {
        this.upLocalCode = upLocalCode;
    }

    public String getUpLocalName() {
        return upLocalName;
    }

    public void setUpLocalName(String upLocalName) {
        this.upLocalName = upLocalName;
    }
}
